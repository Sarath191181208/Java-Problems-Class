public class operators {
    public static void main(String[] args) {
        // Write all the operators in java
        int a = 10;
        int b = 6;

        // Addition
        System.out.println(a + b);
        // Subtraction
        System.out.println(a - b);
        // Multiplication
        System.out.println(a * b);
        // Division
        System.out.println(a / b);
        // Modulus
        System.out.println(a % b);
        // Increment
        System.out.println(a++);
        // Decrement
        System.out.println(a--);
        // Exponent
        System.out.println(a ^ b);
        // Bitwise AND
        System.out.println(a & b);
        // Bitwise OR
        System.out.println(a | b);
        // Bitwise XOR
        System.out.println(a ^ b);
        // Bitwise NOT
        System.out.println(~a);
        // Bitwise Shift Left
        System.out.println(a << b);
        // Bitwise Shift Right
        System.out.println(a >> b);
        // Bitwise Shift Right Unsigned
        System.out.println(a >>> b);

    }
}
